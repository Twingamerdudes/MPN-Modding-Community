    <h2>Introduction to NG++</h2>
    <p>NG++ is a extension to NG+ that is included in Lua for NEM.</p>
    <p>Here are some of NG++'s features:</p>
    <ul>
        <li>Ability to insert new characters into a scene</li>
        <li>Triggers (like in source)</li>
        <li>Interactive scripts (scripts that run when something is interacted with)</li>
        <li>Ability to insert new cutscenes</li>
    </ul>
    <p>NG++ was released on 5/24/2023 with Lua for NEM 1.0.4 p1.</p>
    <p>The p1 implying that this is a part 1 of the update.</p>
