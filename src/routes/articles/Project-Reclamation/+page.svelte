    <h2>What is Project Nexus Reclamation</h2>
    <p>Project Nexus Reclamation or PNR for short is a mod that is planned to be made by <a href="/articles/DarkSignal">DarkSignal</a>.</p>
    <p>The mod was announced on Augest 7th, 2020, along with the announcement of Dossier being repurposed for NEM.</p>
    <p>As of right now, the mod is too big in scope to build using NEM but in the future, when Swain releases official modding tools, it might be worked on.</p>
    <h2>Elements of the mod</h2>
    <p>PNR was to be a full story mod that incorporated RTS elements into M:PN.</p>
    <p>Some of this elements were:</p>
    <ul>
        <li>The ability to recruit and disband allies on the battlefield.</li>
        <li>Player squad wave system in which dead allies would be replaced with infantry that have been deployed to the mission.</li>
        <li>New lose conditions like losing your entire squad and infantry.</li>
    </ul>
    <h2>Story</h2>
    <p>You play after the events of the orginal M:PN where Project Nexus has been destroyed.</p>
    <p>As a result a new group called NEXT has been created to protect what's been salvaged of it.</p>
    <p>Meanwhile the MERC have launched a uprising in the city. They now have new training and are ready to fight the NEXT.</p>
    <p>A lot of the story is unknown simply because the mod hasn't released yet. This article will probably be updated as time goes on.</p>
