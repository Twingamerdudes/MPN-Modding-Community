    <h2>Introduction Zed Survival</h2>
    <p>Zed Survival is a mod made by <a href="articles/Twingamerdudes">Twingamerdudes</a> using <a href="articles/Lua-For-NEM">Lua for NEM</a>.</p>
    <p>In Zed Survival, hordes of zeds try to come after you and you have to fight them off. You can also gain survivors along the way.</p>
    <h2>History</h2>
    <p>Zed Survival was orginally made by XDeafult as a part of one of <a href="articles/NEM">NEM's</a> cheat menus.</p>
    <p>However, this failed, and was broken. It was left as a random button in NEM's cheatmenus, and that's when Twingamerdudes saw it.</p>
    <p>After being disappointed that the mode didn't really work. So he decided to make his own version.</p>
    <p>Twingamerdude's version of Zed Survival was released in early may as a part of Lua for NEM 1.0.3.</p>
    <p>However, it was still very buggy and broke when Lua for NEM was ported over to NEM v53.</p>
    <p>On 6/4/2023 the new version of Zed Survival was released to work with Lua for NEM 1.0.4.1.</p>
