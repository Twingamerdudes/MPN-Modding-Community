<div class="content">
    <p>Twingamerdudes is the creator of this site and is most known for his mod Lua for NEM.</p>
    <p>He has also started side projects like NG+ GUI and NG++ (has now been fused with Lua for NEM).</p>
    <h2>History</h2>
    <p>He started modding M:PN in late October of 2022 with basic personal mods.</p>
    <p>In April of 2023 he started getting into NEM and was disappointed with the lack of programming language implementation that would allow for the changing of the game in real time (yes, I'm a nerd).</p>
    <p>So he started development on Lua for NEM and just one week after development, version 1.0.0 was released to the public.</p>
    <p>It would then get continuous updates until the release of NEM v53 and the beta release of 2.09.b were he worried that starting to port would cause issues when the new update releases.</p>
    <p>Soon after this, NG+ GUI was released since the community wanted a GUI for NG+ while dossier was still updating for NEM v53.</p>
    <p>He also made this site to host the new Stat_Cards API and to provide a place were the community could find resources about M:PN Modding.</p>
